#!/usr/bin/env node
// Generates one kid-friendly example sentence per word (all grades) via
// OpenRouter and writes them to ../sentences.js as `const SENTENCES = {...}`.
// Existing entries in sentences.js are kept — only missing words are filled.
//
// Usage:  OPENROUTER_API_KEY=sk-or-... node tools/generate-sentences.mjs

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const API_KEY = process.env.OPENROUTER_API_KEY;
if (!API_KEY) {
  console.error("Missing OPENROUTER_API_KEY environment variable.");
  process.exit(1);
}

const MODEL = "google/gemini-2.5-flash";
const CHUNK = 60;

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const wordsSrc = readFileSync(join(root, "words.js"), "utf8");
const WORDS = new Function(`${wordsSrc}; return WORDS;`)();
const allWords = [...new Set(Object.values(WORDS).flat())].sort();

const sentencesPath = join(root, "sentences.js");
let SENTENCES = {};
if (existsSync(sentencesPath)) {
  SENTENCES = new Function(`${readFileSync(sentencesPath, "utf8")}; return SENTENCES;`)();
}

const missing = allWords.filter((w) => !SENTENCES[w]);
console.log(`${allWords.length} unique words, ${missing.length} need sentences`);

async function chat(prompt) {
  for (let attempt = 1; attempt <= 5; attempt++) {
    const res = await fetch("https://openrouter.ai/api/v1/chat/completions", {
      method: "POST",
      headers: { Authorization: `Bearer ${API_KEY}`, "Content-Type": "application/json" },
      body: JSON.stringify({ model: MODEL, messages: [{ role: "user", content: prompt }] }),
    });
    if (res.status === 429 || res.status >= 500) {
      await new Promise((r) => setTimeout(r, 5000 * attempt));
      continue;
    }
    if (!res.ok) throw new Error(`${res.status} ${await res.text()}`);
    const json = await res.json();
    return json.choices?.[0]?.message?.content ?? "";
  }
  throw new Error("chat: too many retries");
}

function parseJson(text) {
  const m = text.match(/\{[\s\S]*\}/);
  if (!m) throw new Error(`no JSON in response: ${text.slice(0, 120)}`);
  return JSON.parse(m[0]);
}

const containsWord = (sentence, word) =>
  new RegExp(`\\b${word}\\b`, "i").test(sentence);

const okSentence = (s, w) =>
  typeof s === "string" && containsWord(s, w) && s.split(/\s+/).length >= 3 && s.split(/\s+/).length <= 10;

const PROMPT_HEAD =
  "You write example sentences for a children's spelling app (ages 5-10). " +
  "For each word in the list, write ONE short, cheerful, kid-friendly example sentence: " +
  "4 to 8 simple everyday words, present tense preferred, nothing scary or violent. " +
  "The sentence MUST contain the exact word as given (same spelling). " +
  'Return ONLY a JSON object mapping each word to its sentence, like {"cat": "The cat naps on my bed."}.\n\nWords:\n';

for (let i = 0; i < missing.length; i += CHUNK) {
  const chunk = missing.slice(i, i + CHUNK);
  const reply = await chat(PROMPT_HEAD + chunk.join("\n"));
  let obj;
  try {
    obj = parseJson(reply);
  } catch (e) {
    console.log(`chunk ${i / CHUNK + 1}: ${e.message} — will retry words individually`);
    obj = {};
  }
  for (const w of chunk) {
    if (okSentence(obj[w], w)) SENTENCES[w] = obj[w].trim();
  }
  console.log(`chunk ${i / CHUNK + 1}/${Math.ceil(missing.length / CHUNK)} done (${Object.keys(SENTENCES).length} total)`);
}

// individual retries for anything the chunks missed or that failed validation
for (const w of missing.filter((w) => !SENTENCES[w])) {
  for (let attempt = 1; attempt <= 3 && !SENTENCES[w]; attempt++) {
    const reply = await chat(PROMPT_HEAD + w);
    try {
      const obj = parseJson(reply);
      if (okSentence(obj[w], w)) SENTENCES[w] = obj[w].trim();
    } catch { /* retry */ }
  }
  if (!SENTENCES[w]) console.log(`FAILED to get a sentence for "${w}"`);
}

const still = allWords.filter((w) => !SENTENCES[w]);
const lines = allWords
  .filter((w) => SENTENCES[w])
  .map((w) => `  ${JSON.stringify(w)}: ${JSON.stringify(SENTENCES[w])},`)
  .join("\n");
writeFileSync(
  sentencesPath,
  `"use strict";\n\n// Kid-friendly example sentences, one per word (all grades).\n// Generated by tools/generate-sentences.mjs — edit freely, reruns keep edits.\nconst SENTENCES = {\n${lines}\n};\n`
);
console.log(`wrote sentences.js (${allWords.length - still.length}/${allWords.length} words)${still.length ? ` — missing: ${still.join(", ")}` : ""}`);
