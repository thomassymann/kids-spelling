#!/usr/bin/env node
// Reviews sentences.js for ungrammatical, unnatural, or non-kid-friendly
// sentences and patches only the bad ones in place.
//
// Usage:  OPENROUTER_API_KEY=sk-or-... node tools/review-sentences.mjs

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const API_KEY = process.env.OPENROUTER_API_KEY;
if (!API_KEY) {
  console.error("Missing OPENROUTER_API_KEY environment variable.");
  process.exit(1);
}

const MODEL = "google/gemini-2.5-flash";
const CHUNK = 80;

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const sentencesPath = join(root, "sentences.js");
const SENTENCES = new Function(`${readFileSync(sentencesPath, "utf8")}; return SENTENCES;`)();
const words = Object.keys(SENTENCES);

async function chat(prompt) {
  for (let attempt = 1; attempt <= 5; attempt++) {
    const res = await fetch("https://openrouter.ai/api/v1/chat/completions", {
      method: "POST",
      headers: { Authorization: `Bearer ${API_KEY}`, "Content-Type": "application/json" },
      body: JSON.stringify({ model: MODEL, messages: [{ role: "user", content: prompt }] }),
    });
    if (res.status === 429 || res.status >= 500) {
      await new Promise((r) => setTimeout(r, 5000 * attempt));
      continue;
    }
    if (!res.ok) throw new Error(`${res.status} ${await res.text()}`);
    return (await res.json()).choices?.[0]?.message?.content ?? "";
  }
  throw new Error("chat: too many retries");
}

const containsWord = (s, w) => new RegExp(`\\b${w}\\b`, "i").test(s);

const PROMPT =
  "You review example sentences for a children's spelling app (ages 5-10). " +
  "Below is a JSON object mapping words to their example sentence. Find sentences that are " +
  "ungrammatical, unnatural, confusing, or not appropriate for young children, and rewrite " +
  "ONLY those: 4-8 simple words, natural English, must contain the exact word. " +
  "Be conservative — do not rewrite sentences that are fine. " +
  'Return ONLY a JSON object with the corrections, e.g. {"ago": "We moved here long ago."}. ' +
  "Return {} if everything is fine.\n\n";

let fixed = 0;
for (let i = 0; i < words.length; i += CHUNK) {
  const chunk = Object.fromEntries(words.slice(i, i + CHUNK).map((w) => [w, SENTENCES[w]]));
  const reply = await chat(PROMPT + JSON.stringify(chunk, null, 1));
  const m = reply.match(/\{[\s\S]*\}/);
  if (!m) continue;
  let corrections;
  try {
    corrections = JSON.parse(m[0]);
  } catch {
    console.log(`chunk ${i / CHUNK + 1}: unparseable reply, skipped`);
    continue;
  }
  for (const [w, s] of Object.entries(corrections)) {
    if (chunk[w] && typeof s === "string" && containsWord(s, w) && s.split(/\s+/).length <= 10) {
      console.log(`fix    ${w}: "${SENTENCES[w]}" → "${s}"`);
      SENTENCES[w] = s.trim();
      fixed++;
    }
  }
  console.log(`chunk ${i / CHUNK + 1}/${Math.ceil(words.length / CHUNK)} reviewed`);
}

const lines = words
  .sort()
  .map((w) => `  ${JSON.stringify(w)}: ${JSON.stringify(SENTENCES[w])},`)
  .join("\n");
writeFileSync(
  sentencesPath,
  `"use strict";\n\n// Kid-friendly example sentences, one per word (all grades).\n// Generated by tools/generate-sentences.mjs — edit freely, reruns keep edits.\nconst SENTENCES = {\n${lines}\n};\n`
);
console.log(`Done — ${fixed} sentences fixed.`);
